# Иницијализација променљивих
# i - бројач бројева који испуњавају услов (2 ≤ број ≤ 6)
# s - сума бројева који испуњавају услов (2 ≤ број ≤ 6)
i = 0
s = 0

# Обавештење кориснику да почне да уноси бројеве
print("Unesite niz realnih brojeva ili 0 za kraj: ")

# Бесконачна петља која се извршава док корисник не унесе 0
while True:
    # Унос броја од стране корисника и конверзија у реалан број
    x = float(input())

    # Проверава се да ли је унет број 0, што означава крај уноса
    if x == 0:
        # Рачунање аритметичке средине као количник суме и броја елемената
        sredina = s / i
        # Приказ резултата заокруженог на 3 децимале
        print("Aritmeticka sredina =", round(sredina, 3))
        # НАПОМЕНА: Недостаје break наредба да би се петља прекинула!

    # Проверава се да ли је број у опсегу [2, 6]
    if x >= 2 and x <= 6:
        # Ако јесте, додаје се вредност броју на суму
        s = s + x
        # Увећава се бројач за 1
        i = i + 1
