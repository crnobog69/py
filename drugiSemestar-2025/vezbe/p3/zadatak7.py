# Програм рачуна суму бесконачног реда 1 + 1/4 + 1/9 + 1/16 + ... + 1/(i^2) + ...
# док вредност члана не постане мања од 1e-4

# Почетне вредности променљивих
i = 1  # Бројач (индекс члана низа)
clan = 1  # Први члан низа је 1/(1^2) = 1
s = 0  # Почетна вредност суме

# 1e-4 је научна нотација у Пајтону која представља број 10^(-4),
# односно 0.0001 (једна десетохиљадити део јединице).

# Сабирамо чланове док је вредност члана већа од 0.0001
while clan > 1e-4:
    s = s + clan  # Додајемо текући члан на суму
    i = i + 1  # Прелазимо на наредни индекс
    clan = 1 / (i * i)  # Рачунамо нови члан низа као 1/(i^2)

# Исписујемо резултат заокружен на 3 децимале
print(f"S = {round(s, 3)}")
