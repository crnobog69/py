# Иницијализација променљиве i са вредношћу 1
i = 1

# Петља која се извршава док је i мање или једнако 5
# Петља се користи за исписивање поруке "Поздрав" пет пута
while i <= 5:
    # Исписује "Поздрав" на екран
    print("Поздрав")
    # Повећава вредност i за 1
    # Ово осигурава да се петља заврши након 5 итерација
    i = i + 1
