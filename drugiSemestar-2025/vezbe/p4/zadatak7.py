# Задатак 68: Програм за рачунање суме факторијела бројева од 1 до n

# Дефиниција рекурзивне функције за израчунавање факторијела броја n
def fakt(n):
    # Базни случај: факторијел броја 1 је 1
    if n == 1:
        return 1
    # Рекурзивни корак: n! = n * (n-1)!
    return n * fakt(n - 1)


# Учитавање вредности n од корисника
n = int(input("n = "))

# Иницијализација променљиве за суму факторијела
s = 0

# Петља за израчунавање суме факторијела свих бројева од 1 до n
for i in range(1, n + 1, 1):
    # Додавање факторијела броја i на укупну суму
    s = s + fakt(i)

# Приказ резултата кориснику
print(f"s = {s}")
