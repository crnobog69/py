# Програм рачуна суму: 1/(n+m) - 1/(n+2m) + 1/(n+3m) - ... + (-1)^(m-1)/(n+m*m)
# где су n и m вредности које корисник уноси

# Прихватање улазних података од корисника
n = int(input("Унесите n = "))  # Улазна вредност n (основа имениоца)
m = int(
    input("Унесите m = ")
)  # Улазна вредност m (одређује број чланова и утиче на именилац)

s = 0  # Променљива за чување суме серије
znak = 1  # Променљива за промену знака (+1 или -1)

# Петља која израчунава суму серије
for i in range(1, m + 1, 1):
    # Додајемо i-ти члан серије (znak / (n + i * m))
    # За i=1: 1/(n+m), за i=2: -1/(n+2m), за i=3: 1/(n+3m), итд.
    s = s + znak / (n + i * m)

    # Мењамо знак за следећу итерацију (1 постаје -1, -1 постаје 1)
    znak = -znak

# Приказујемо резултат са 5 децималних места
print(f"Сума = {s:.5f}")

