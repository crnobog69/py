# Саставити програм који за унето n рачуна суму. 1 / i*i
# Програм треба да ради за произвољан број уноса n све док за његову вредност не прочита нулу или негативни број.
while True:
    n = int(input("\nn = "))

    if n <= 0:
        break

    suma = 0

    for i in range(1, n + 1, 1):
        suma = suma + 1 / (i * i)

    print(f"Сума = {suma:.5f}")
