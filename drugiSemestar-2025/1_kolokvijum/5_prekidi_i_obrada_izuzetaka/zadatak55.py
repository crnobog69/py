# Овај програм користи петљу која пролази кроз бројеве од 1 до 10.
# Када наиђе на непаран број (остатак при дељењу са 2 није 0),
# користи наредбу 'continue' која прескаче остатак тренутне итерације
# и прелази на следећу.
# Резултат је да се исписују само парни бројеви: 2, 4, 6, 8, 10.

for i in range(1, 11, 1):
    if i % 2 != 0:
        continue
    print(i)
