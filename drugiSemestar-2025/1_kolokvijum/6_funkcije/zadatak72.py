# Саставити рекурзивну функцију која сабира декадне цифре целог броја.
# Затим тестирати функцију у главном програму за унети цео број и исписати добијени резултат.
# Главни део програма обрађује произвољан број уноса.


# Дефиниција рекурзивне функције SaberiCifre која сабира све цифре броја
# Параметри:
#   x - цео број чије цифре треба сабрати
# Повратна вредност:
#   Сума свих цифара броја x
def SaberiCifre(x):
    # Проверавамо да ли је број негативан
    # Ако јесте, претварамо га у позитиван јер нас занимају само његове цифре
    if x < 0:
        x = abs(x)

    # Базни случај рекурзије: када је број једноцифрен
    # враћамо сам тај број јер је једина цифра у њему
    if x < 10:
        return x

    # Рекурзивни корак:
    # 1. x % 10 - издвајамо последњу цифру броја (остатак при дељењу са 10)
    # 2. x // 10 - одсецамо последњу цифру (целобројно дељење са 10)
    # 3. SaberiCifre(x // 10) - рекурзивно позивамо функцију за преостали део броја
    # На крају сабирамо последњу цифру са сумом осталих цифара
    return x % 10 + SaberiCifre(x // 10)


# Главни део програма - бесконачна петља за обраду више уноса
while True:
    # Учитавамо цео број од корисника
    n = int(input("n = "))

    # Позивамо функцију за сабирање цифара и чувамо резултат
    s = SaberiCifre(n)

    # Исписујемо резултат - суму цифара унетог броја
    print(f"Сума цифара вроја {n} = {s}")
    # Исписујемо линију која раздваја различите уносе ради боље прегледности
    print("------------------------------")
