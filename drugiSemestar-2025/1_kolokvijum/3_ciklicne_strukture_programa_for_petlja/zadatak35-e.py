# Учитавамо број редова троугла од корисника
n = int(input("n = "))

# Спољашња петља која контролише број редова
# Променљива i представља тренутни ред (од 1 до n)
for i in range(1, n + 1, 1):
    # Прва унутрашња петља исцртава размаке пре звездица
    # Почињемо са (n-1) размака у првом реду и смањујемо за сваки следећи ред
    for k in range(n, i, -1):
        print(" ", end="")

    # Друга унутрашња петља исцртава звездице са размацима
    # Број звездица је једнак броју реда (i)
    for j in range(1, i + 1, 1):
        print("* ", end="")

    # Након што завршимо са исцртавањем једног реда, прелазимо у нови ред
    print()
